#DISSECANDO VARIAVEIS
var = input ('digite algo: ')
print ('o tipo dessa variavel é: ', type(var))
print ('possui espaços? ', var.isspace())
print ('tem maiusculo?', var.isupper())
print ('é numérica? ', var.isnumeric())
print ('é um alfabético? ', var.isalpha())
print ('é decimal? ', var.isdecimal())
print ('é alfanumérico? ', var.isalnum())